public class Number {
    private int num;
    private int count;

    public Number(int num, int count) {
        this.num = num;
        this.count = count;
    }

    public Number() {

    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
